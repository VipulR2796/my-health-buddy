    private void btnAdd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd1ActionPerformed
        // TODO add your handling code here:
        
        String item = comboFood1.getSelectedItem().toString();
        String quantity = comboQuantity1.getSelectedItem().toString();
        float qty = convertQuantityStringToFloat(quantity);
        System.out.println(qty);
        
        float calories = 0, protein = 0, carbs = 0, fats = 0, sodium = 0, cholesterol = 0;
        try {
            
            ProductDirectory pd = new ProductDirectory();

            ResultSet res = pd.exactNutritionLookup(item);
//        System.out.print(res + "YYYY");
            while(res.next()){
                
                calories = (qty * res.getFloat("perc_calories"))/100;
                protein = (qty * res.getFloat("perc_protein"))/100;
                carbs = (qty * res.getFloat("perc_carbs"))/100;
                fats = (qty * res.getFloat("perc_fats"))/100;   
                sodium = (qty * res.getFloat("perc_sodium"))/100;
                cholesterol = (qty * res.getFloat("perc_cholesterol"))/100; 
                System.out.println("@@@@");
                System.out.println(calories);
            }
        } catch (SQLException ex) {
            System.out.println("perc calc failed");
            Logger.getLogger(UserDailyIntake.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        updateDailyIntakeTable(calories, protein, carbs, fats, sodium, cholesterol);
        
        
        showDietInfo(this.userName);
    }//GEN-LAST:event_btnAdd1ActionPerformed

