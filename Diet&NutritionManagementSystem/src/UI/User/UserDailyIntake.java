    public float convertQuantityStringToFloat(String qty){
        float qtyFloat = 0;
        Connection dbconn = DBconnection.connectDB();
            PreparedStatement st;
        
        try{
            String query = "SELECT quantity_gm from quantitymapping WHERE measure=?";
            st = (PreparedStatement)dbconn.prepareStatement(query);
            st.setString(1, qty); 
            ResultSet res = st.executeQuery();
            while(res.next()){
                qtyFloat = res.getFloat("quantity_gm");
                System.out.println("inside" + qtyFloat);
            }
        }
        catch(SQLException ex){
            Logger.getLogger(UserDailyIntake.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("----" + qtyFloat);
        return qtyFloat;   
    }
    
